    public void getTransportationTypeData(){
        List<TransportationType> transportationTypes = transportationTypeBUS.getAll();
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        int i=0;
        for(TransportationType transportationType: transportationTypes){
            Object row[] = new Object[]{++i,transportationType.getName(),transportationType.getBrand(),transportationType.getType(),transportationType.getId()};
            model.addRow(row);
        }
    }
    public void getTransportationTypeDetail(){
        refresh();
        if(selectedTransportationType!=null){
            nameTxt.setText(selectedTransportationType.getName());
            brandTxt.setText(selectedTransportationType.getBrand());
            typeTxt.setText(selectedTransportationType.getType());
        }else {
            JOptionPane.showMessageDialog(contentPanel2, "Có lỗi xảy ra!!Tiến hành tải lại trang...");
            getTransportationTypeData();
        }
    }
    public void refresh(){
        nameTxt.setText("");
        brandTxt.setText("");
        typeTxt.setText("");
    }
    public boolean validateField(){
        if(nameTxt.getText().isBlank()||brandTxt.getText().isBlank()||typeTxt.getText().isBlank()){
            JOptionPane.showMessageDialog(contentPanel2, "Vui lòng không để trống thông tin");
            return false;
        }
        return true;
    }
