    private void button4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button4ActionPerformed
        if(accountBUS.getById(account.getId()).getRole().getTransportationTypeManager()!=2){
            JOptionPane.showMessageDialog(contentPanel2, "Bạn không có quyền thao tác");
            return;
        }
        if(!validateField())
            return;
        String name = nameTxt.getText();
        String brand = brandTxt.getText();
        String type = typeTxt.getText();
        TransportationType createTransportationType = new TransportationType(name, brand, type);
        if(transportationTypeBUS.save(createTransportationType)){
            JOptionPane.showMessageDialog(contentPanel2, "Thêm thành công");
            refresh();
        }else{
            JOptionPane.showMessageDialog(contentPanel2, "Có lỗi xảy ra!!");
        }
        getTransportationTypeData();
    }//GEN-LAST:event_button4ActionPerformed

    private void button5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button5ActionPerformed
        if(accountBUS.getById(account.getId()).getRole().getTransportationTypeManager()!=2){
            JOptionPane.showMessageDialog(contentPanel2, "Bạn không có quyền thao tác");
            return;
        }
        if(selectedTransportationType==null){
            JOptionPane.showMessageDialog(contentPanel2, "Vui lòng chọn đối tượng thể thao tác");
            return;
        }
        if(!validateField())
            return;
        String name = nameTxt.getText();
        String brand = brandTxt.getText();
        String type = typeTxt.getText();
        selectedTransportationType.setName(name);
        selectedTransportationType.setBrand(brand);
        selectedTransportationType.setType(type);
        if(transportationTypeBUS.save(selectedTransportationType)){
            JOptionPane.showMessageDialog(contentPanel2, "Cập nhật thành công");
            refresh();
            selectedTransportationType=null;
        }else{
            JOptionPane.showMessageDialog(contentPanel2, "Có lỗi xảy ra!!");
        }
        getTransportationTypeData();
    }//GEN-LAST:event_button5ActionPerformed

    private void button7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button7ActionPerformed
        if(accountBUS.getById(account.getId()).getRole().getTransportationTypeManager()!=2){
            JOptionPane.showMessageDialog(contentPanel2, "Bạn không có quyền thao tác");
            return;
        }
        if(selectedTransportationType!=null){
            if(transportationTypeBUS.delete(selectedTransportationType)){
                JOptionPane.showMessageDialog(contentPanel2, "Xóa thành công");
                refresh();
            }
            else 
                JOptionPane.showMessageDialog(contentPanel2,"Vẫn còn dữ liệu xe thuộc dòng xe này!! Không thể xóa");
            getTransportationTypeData();
        }else {
            JOptionPane.showMessageDialog(contentPanel2, "Vui lòng chọn một đối tượng để thao tác");
        }
    }//GEN-LAST:event_button7ActionPerformed

    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        String keyword = searchTxt.getText();
        List<TransportationType> transportationTypes = transportationTypeBUS.getByNameKeyword(keyword);
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        int i=0;
        for(TransportationType transportationType: transportationTypes){
            Object row[] = new Object[]{++i,transportationType.getName(),transportationType.getBrand(),transportationType.getType(),transportationType.getId()};
            model.addRow(row);
        }
    }//GEN-LAST:event_searchActionPerformed


