    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        if (evt.getClickCount() == 2) {
            JTable target = (JTable) evt.getSource();
            int row = target.getSelectedRow();
            String cmnd = table.getValueAt(row, 0).toString();
            selectedOwner = ownerBUS.getByCmnd(cmnd);
            setOwnerDetail(selectedOwner);
        }
    }//GEN-LAST:event_tableMouseClicked

    private void button4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button4ActionPerformed
        if(accountBUS.getById(account.getId()).getRole().getOwnerManager()!=2){
            JOptionPane.showMessageDialog(contentPanel2, "Bạn không có quyền thao tác");
            return;
        }
        try{
            if(!validateField())
                return;
            String name = nameTxt.getText();
            String cmnd = cmndTxt.getText();
            String gender = genderOption.getSelection()==null?null:maleOption.isSelected()?"Nam":"Nữ";
            String address = addressTxt.getText();
            String phone = phoneTxt.getText();
            Date birthday = birthdateField.getDate();
            Owner createOwner = new Owner(name, birthday, cmnd, address, phone, gender);
            if(ownerBUS.save(createOwner)){
                JOptionPane.showMessageDialog(contentPanel2, "Thêm thành công!!");
                refresh();
            }else{
                JOptionPane.showMessageDialog(contentPanel2, "Thêm thất bại!!Hãy chắc chắn CMND chưa đựọc sử dụng");
            }
            setOwnersData();
            
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_button4ActionPerformed

    private void button8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button8ActionPerformed
        if(selectedOwner==null){
            JOptionPane.showMessageDialog(contentPanel2, "Vui lòng chọn một đối tượng để xem!!");
            return;
        }
        
        DriverLicense driverLicense=selectedOwner.getDriverLicense();
        if(driverLicense!=null){
            licenseId.setText(driverLicense.getId());
            licenseClass.setText(driverLicense.getHang());
            licenseDate.setDate(driverLicense.getCreatedDate());
            licenseAddress.setText(driverLicense.getAddress());
        }else{
            licenseId.setText("");
            licenseClass.setText("");
            licenseAddress.setText("");
        }
        driverInfo.setVisible(true);
    }//GEN-LAST:event_button8ActionPerformed

