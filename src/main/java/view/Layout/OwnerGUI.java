    public void setOwnerDetail(Owner selectedOwner){
        refresh();
        if(selectedOwner!=null){
            nameTxt.setText(selectedOwner.getFullName());
            cmndTxt.setText(selectedOwner.getCmnd());
            if(selectedOwner.getGender().equals("Nam"))
                maleOption.setSelected(true);
            else femaleOption.setSelected(true);
            addressTxt.setText(selectedOwner.getAddress());
            phoneTxt.setText(selectedOwner.getPhoneNumber());
            birthdateField.setDate(selectedOwner.getBirthday());
        }
        else {
            JOptionPane.showMessageDialog(parentFrame, "Có lỗi xảy ra, tiến hành tải lại trang...");
            setOwnersData();
        }
    }
    public void setOwnersData(){
        List<Owner> ownerList = ownerBUS.getAll();
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        for(Owner owner:ownerList){
            Object row[] = new Object[]{owner.getCmnd(),owner.getFullName()};
            model.addRow(row);
        }
    }
    public void updateDriverLicense(){
        String licenseId = this.licenseId.getText();
        String licenseClass = this.licenseClass.getText();
        String licenseAddress = this.licenseAddress.getText();
        Date licenseDate = this.licenseDate.getDate();
        if(licenseId==null||licenseClass==null||licenseAddress==null){
            JOptionPane.showMessageDialog(contentPanel2, "Không được để trống thông tin");
            return;
        }
        if(licenseDate.after(new Date())){
            JOptionPane.showMessageDialog(contentPanel2, "Ngày sinh không hợp lệ");
            return;
        }
        DriverLicense driverLicense = new DriverLicense(licenseId, licenseClass, licenseDate, licenseAddress);
        if(ownerBUS.saveDriverLicense(selectedOwner, driverLicense)){
            JOptionPane.showMessageDialog(contentPanel2, "Cập nhật thành công");
            driverInfo.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(contentPanel2, "ID của giấy phép không được trùng với người khác!");
        }
        
    }
    
    public boolean validateField(){
        String name = nameTxt.getText();
        String cmnd = cmndTxt.getText();
        String gender = genderOption.getSelection()==null?null:maleOption.isSelected()?"Nam":"Nữ";
        String address = addressTxt.getText();
        String phone = phoneTxt.getText();
        String phoneRegex = "^(0[0-9]{9}|\\+84[0-9]{9})$";
        String cmndRegex = "\\d{12}";
        Date birthday = birthdateField.getDate();
        if(name.isBlank() || cmnd.isBlank()||gender==null||address.isBlank()||phone.isBlank()){
            JOptionPane.showMessageDialog(contentPanel2, "Không được để trống thông tin");
            return false;
        }
        if(birthday.after(new Date())){
            JOptionPane.showMessageDialog(contentPanel2, "Ngày sinh không hợp lệ");
            return false;
        }
        if(!Pattern.compile(phoneRegex).matcher(phone).matches()){
            JOptionPane.showMessageDialog(this, "Định dạng số điện thoại 0xxx-xxx-xxx hoặc +84-xxx-xxx-xxx");
            return false;
        }
        if(!Pattern.compile(cmndRegex).matcher(cmnd).matches()){
            JOptionPane.showMessageDialog(this, "Định dạng số cmnd là 12 số");
            return false;
        }
        return true;
    }
    
    
