    private void button7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button7ActionPerformed
        if(accountBUS.getById(account.getId()).getRole().getOwnerManager()!=2){
            JOptionPane.showMessageDialog(contentPanel2, "Bạn không có quyền thao tác");
            return;
        }
        if(selectedOwner!=null){
            if(ownerBUS.delete(selectedOwner)){
                JOptionPane.showMessageDialog(parentFrame, "Xóa thành công");
                
            }else {
                JOptionPane.showMessageDialog(parentFrame, "Có lỗi xảy ra!! Tiến hành tải lại trang...");
            }
            refresh();
            setOwnersData();
        }else {
            JOptionPane.showMessageDialog(parentFrame, "Vui lòng chọn một đối tượng để thao tác");
        }
    }//GEN-LAST:event_button7ActionPerformed

    private void button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button3ActionPerformed
        String cmnd = searchTxt.getText();
        List<Owner>ownerList = ownerBUS.getByCmndKeyword(cmnd);
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        for(Owner owner:ownerList){
            Object row[] = new Object[]{owner.getCmnd(),owner.getFullName()};
            model.addRow(row);
        }
        
    }//GEN-LAST:event_button3ActionPerformed
