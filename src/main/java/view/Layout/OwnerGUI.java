    private void button5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button5ActionPerformed
        if(accountBUS.getById(account.getId()).getRole().getOwnerManager()!=2){
            JOptionPane.showMessageDialog(contentPanel2, "Bạn không có quyền thao tác");
            return;
        }
        try{
            if(!validateField())
                return;
            String name = nameTxt.getText();
            String cmnd = cmndTxt.getText();
            String gender = genderOption.getSelection()==null?null:maleOption.isSelected()?"Nam":"Nữ";
            String address = addressTxt.getText();
            String phone = phoneTxt.getText();
            Date birthday = birthdateField.getDate();
            selectedOwner.setFullName(name);
            selectedOwner.setAddress(address);
            selectedOwner.setBirthday(birthday);
            selectedOwner.setCmnd(cmnd);
            selectedOwner.setGender(gender);
            selectedOwner.setPhoneNumber(phone);
            if(ownerBUS.save(selectedOwner)){
                JOptionPane.showMessageDialog(contentPanel2, "Cập nhật thành công!!");
                refresh();
                selectedOwner=null;
            }else{
                JOptionPane.showMessageDialog(contentPanel2, "Thêm thất bại!!Hãy chắc chắn CMND chưa đựọc sử dụng");
            }
            setOwnersData();
            
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_button5ActionPerformed

